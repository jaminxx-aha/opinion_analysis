def generate_row(i, desc):
    """生成一行完整数据"""
    # 根据描述推断优先级
    high_keywords = ["闪退", "崩溃", "失败", "断流", "黑屏", "白屏"]
    if any(kw in desc for kw in high_keywords):
        priority = random.choices(["高", "中", "低"], weights=[50, 35, 15])[0]
    else:
        priority = random.choices(["高", "中", "低"], weights=[20, 45, 35])[0]

    status = random.choices(STATUSES, weights=[30, 35, 25, 10])[0]
    handler = random.choice(HANDLERS) if status in ["已解决", "已关闭"] else "未分配"

    start = datetime(2024, 1, 1)
    end = datetime(2024, 12, 31)
    date = (start + timedelta(days=random.randint(0, (end - start).days))).strftime("%Y-%m-%d")
    time = f"{random.randint(0,23):02d}:{random.randint(0,59):02d}"

    return {
        "序号": i,
        "应用名": "抖音",
        "日期": date,
        "时间": time,
        "问题描述": desc,
        "用户ID": f"U{random.randint(1,999999)}",
        "渠道来源": random.choice(CHANNELS),
        "设备类型": random.choice(DEVICES),
        "系统版本": random.choice(OS_VERSIONS),
        "问题状态": status,
        "优先级": priority,
        "处理人员": handler,
    }


def generate_100_rows():
    rows = []
    idx = 1

    # 1. 未知问题：15条
    for desc in random.sample(UNKNOWN_PROBLEMS, min(15, len(UNKNOWN_PROBLEMS))):
        rows.append(generate_row(idx, desc))
        idx += 1

    # 2. 仅一级分类：约20条（8类各2-3条）
    for level1, descs in LEVEL1_ONLY.items():
        sampled = random.sample(descs, min(2, len(descs)))
        for desc in sampled:
            rows.append(generate_row(idx, desc))
            idx += 1
    # 再随机补几条一级分类使总数达到20
    all_l1 = []
    for level1, descs in LEVEL1_ONLY.items():
        all_l1.extend(descs)
    while len(rows) < 15 + 20:
        rows.append(generate_row(idx, random.choice(all_l1)))
        idx += 1

    # 3. 仅二级分类：约20条
    all_l2 = []
    for l1, subs in LEVEL2_ONLY.items():
        for l2, descs in subs.items():
            all_l2.extend(descs)
    sampled = random.sample(all_l2, min(20, len(all_l2)))
    for desc in sampled:
        rows.append(generate_row(idx, desc))
        idx += 1

    # 4. 全三级分类：剩余条数（约45条）
    all_l3 = []
    for l1, subs in LEVEL3_FULL.items():
        for l2, descs in subs.items():
            all_l3.extend(descs)
    remaining = 100 - idx + 1
    sampled = random.sample(all_l3, min(remaining, len(all_l3)))
    for desc in sampled:
        rows.append(generate_row(idx, desc))
        idx += 1

    # 如果不够100条，用三级分类数据补充
    while len(rows) < 100:
        rows.append(generate_row(idx, random.choice(all_l3)))
        idx += 1

    return rows[:100]


