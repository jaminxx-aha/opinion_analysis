# 分类权重（卡顿类最多，符合真实舆情分布）
CATEGORY_WEIGHTS = {
    "卡顿": 30,
    "响应慢/延迟": 15,
    "闪退/崩溃": 15,
    "启动异常": 8,
    "发热": 8,
    "内存异常": 5,
    "渲染异常": 10,
    "网络异常": 9,
}

def generate_problem_desc():
    """根据权重随机生成问题描述"""
    categories = list(CATEGORY_WEIGHTS.keys())
    weights = list(CATEGORY_WEIGHTS.values())
    category = random.choices(categories, weights=weights, k=1)[0]
    template = random.choice(PROBLEM_TEMPLATES[category])
    prefix = random.choice(TIME_PREFIXES)
    if prefix:
        return f"{prefix}{template}"
    return template

def generate_date():
    """生成2024年随机日期"""
    start = datetime(2024, 1, 1)
    end = datetime(2024, 12, 31)
    delta = (end - start).days
    random_date = start + timedelta(days=random.randint(0, delta))
    return random_date.strftime("%Y-%m-%d")

def generate_time():
    """生成随机时间 HH:MM"""
    hour = random.randint(0, 23)
    minute = random.randint(0, 59)
    return f"{hour:02d}:{minute:02d}"

def generate_user_id():
    """生成随机用户ID"""
    return f"U{random.randint(10000, 999999)}"

def main():
    wb = openpyxl.Workbook()
    ws = wb.active
    ws.title = "抖音舆情数据"

    # 表头
    headers = ["序号", "应用名", "日期", "时间", "问题描述", "用户ID", "渠道来源", "设备类型", "系统版本", "问题状态", "优先级", "处理人员"]
    ws.append(headers)

    # 生成数据
    for i in range(1, 10001):
        row = [
            i,
            "抖音",
            generate_date(),
            generate_time(),
            generate_problem_desc(),
            generate_user_id(),
            random.choice(CHANNELS),
            random.choice(DEVICES),
            random.choice(SYSTEM_VERSIONS),
            random.choice(STATUSES),
            random.choice(PRIORITIES),
            random.choice(HANDLERS),
        ]
        ws.append(row)

    output_path = "test/抖音舆情数据_10000.xlsx"
    wb.save(output_path)
    print(f"生成完成: {output_path} (10000行)")

if __name__ == "__main__":
    main()