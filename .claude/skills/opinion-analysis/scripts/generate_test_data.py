def generate_user_id():
    """生成用户ID"""
    return f"U{random.randint(100000, 999999)}"

def generate_date():
    """生成日期（最近30天内）"""
    from datetime import datetime, timedelta
    base = datetime(2024, 1, 15)
    delta = timedelta(days=random.randint(0, 30))
    date = base + delta
    return date.strftime("%Y-%m-%d")

def generate_channel():
    """生成渠道"""
    channels = ["App Store", "应用市场", "官方客服", "12315", "微博投诉", "公众号反馈", "电话投诉"]
    return random.choice(channels)

def generate_problem(app):
    """生成问题描述"""
    # 根据权重选择应用
    if app is None:
        total_weight = sum(a[1] for a in apps)
        r = random.randint(1, total_weight)
        cumulative = 0
        for a, w in apps:
            cumulative += w
            if r <= cumulative:
                app = a
                break

    # 选择模块
    modules = list(app_templates[app].keys())
    module = random.choice(modules)

    # 选择问题描述
    problems = app_templates[app][module]
    problem = random.choice(problems)

    # 有时添加一些前缀使描述更真实
    prefixes = [
        "",
        "今天",
        "这几天",
        "从昨天开始",
        "更新后",
        "最近",
        "刚才",
        "用的时候",
        "每次",
    ]
    prefix = random.choice(prefixes)

    if prefix:
        problem = f"{prefix}{problem}"

    return app, module, problem

def main():
    """生成10000条数据"""
    data = []

    for i in range(10000):
        user_id = generate_user_id()
        date = generate_date()
        channel = generate_channel()
        app, module, problem = generate_problem(None)

        data.append({
            "用户ID": user_id,
            "日期": date,
            "渠道": channel,
            "应用名": app,
            "问题描述": problem,
        })

    # 创建DataFrame
    df = pd.DataFrame(data)

    # 保存Excel
    output_path = "舆情数据示例_10000.xlsx"
    df.to_excel(output_path, index=False)

    print(f"已生成 {len(data)} 条数据，保存到: {output_path}")

    # 打印统计
    print("\n应用分布:")
    print(df["应用名"].value_counts())

if __name__ == "__main__":
    main()